metro = float(input("Digite a distância em metros: "))

decimetro = (metro * 10)
centimetro = (metro * 100)
milimetro = (metro * 1000)
quilometro = (metro / 1000)

print(f"Em decimetro é {decimetro}")
print(f"Em centimetro é {centimetro}")
print(f"Em milimetro é {milimetro}")
print(f"Em quilometro é {quilometro}")